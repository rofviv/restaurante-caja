    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    Core.CONEXION = new ConexionDB().conexion();
                    //String ruta = "src/reportes/";
                    String ruta = "C:/restaurante/reportes/";
                    Core.TICKET = JasperCompileManager.compileReport(ruta + "Ticket.jrxml");
                    Core.TCOCINA = JasperCompileManager.compileReport(ruta + "TicketCocina.jrxml");
                    Core.ARQUEO = JasperCompileManager.compileReport(ruta + "Arqueo.jrxml");
                    Core.DETALLE_VENTA = JasperCompileManager.compileReport(ruta + "VentasDiarias.jrxml");
                    //Core.DETALLE_VENTA = (JasperReport) JRLoader.loadObject("VentasDiarias.jasper");
                    Core.RESUMEN_VENTA = JasperCompileManager.compileReport(ruta + "ResumenVentas.jrxml");
                    dispose();
                    new LoguinPresentacion().setVisible(true);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "No se puede iniciar " + ex.getMessage(), "Error al Iniciar", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                }
            }
        }).start();
    }//GEN-LAST:event_formWindowOpened

