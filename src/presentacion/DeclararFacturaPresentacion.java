    public void cargarLista(ArrayList<Recibo> arFactura) {
        this.arFactura = arFactura;
        for (Recibo r : arFactura) {
            lstModel.addElement("Factura " + r.getNroRecibo() + " de " + r.getMonto() + " bolivianos.");
        }
        lstDeclarados.setModel(lstModel);
        calcular();
    }
    
    public ArrayList<Recibo> getListaDeclarados() {
        return arFactura;
    }
    
    public double sumaFactura() {
        return total;
    }

    private void limpiarCampos() {
        txtNroFactura.setText("");
        txtConcepto.setText("");
        txtMonto.setText("");
        txtNroFactura.requestFocus();
    }

    private void calcular() {
        double suma = 0.0;
        for (Recibo r : arFactura) {
            suma += r.getMonto();
        }
        txtSuma.setText(suma + "");
        total = suma;
    }

