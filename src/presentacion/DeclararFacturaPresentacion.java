    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!txtNroFactura.getText().equals("") && !txtConcepto.getText().equals("") && !txtMonto.getText().equals("")) {
            arFactura.add(new Recibo(txtNroFactura.getText(), txtConcepto.getText(), Double.parseDouble(txtMonto.getText())));
            lstModel.addElement("Factura " + txtNroFactura.getText() + " de " + Double.parseDouble(txtMonto.getText()) + " bolivianos.");
            lstDeclarados.setModel(lstModel);
            limpiarCampos();
            calcular();
        } else {
            JOptionPane.showMessageDialog(null, "Los campos deben estar llenos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int pos = arFactura.size() - 1;
        if (pos >= 0) {
            arFactura.remove(pos);
            lstModel.remove(pos);
            lstDeclarados.setModel(lstModel);
            calcular();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

