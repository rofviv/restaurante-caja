package entidad;

/**
 *
 * @author Carlax
 */
public class GuarnicionEntidad {
    private int id;
    private String referencia, titulo, tipo;

    public GuarnicionEntidad() {
        id = 0;
        referencia = titulo = tipo = "";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
}
